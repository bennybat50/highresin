        <p>The term “Whale” comes from exactly that. Whales are the largest animal in the ocean and can “eat up” much smaller fish without much hassle. Just like the ocean, Bitcoin Whales can control much smaller retail traders with their power to move the entire market price. This principle can be devastating for Altcoin prices due to the significantly lower level of liquidity and market capitalisations. In order to drive prices lower, Whales often sell large quantities below the current price, and as the market starts to panic and follow selling off, the Whales simply re-buy what they had sold, or even larger quantities to increase their holdings. They may even “bluff” retail traders into doing this by placing a big sell order on the order books, which will naturally attract the price lower. The Whales then simply remove the order and buy more at a lower price.

            Whales do not necessarily always practice in this manner, it is not always in their interest to disrupt the entire market. In order to buy or sell large quantities of assets, they bypass the exchanges via a service called “Over The Counter” (OTC) trading. This is an “off-record” transaction that usually occurs directly between the exchange and the purchasing body.</p>
