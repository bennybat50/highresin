        <p>Between total supply and circulating supply, traders pay more attention to circulating supply, as the coins that are not available for public trading, are not taken into account in the calculation of the market cap. Also, circulating supply has higher importance as the magnitude of the influx of coins could affect the price of the asset. All in all, both metrics are hugely important. As a last note, when there is a huge discrepancy between Total Supply and Circulating Supply, traders must do deeper research to know the reasons of the incoherent proportion.</p>
